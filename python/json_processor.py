class JsonProcessor:

    @classmethod
    def judge_special_str_in_json_key(cls, data, special_str: str) -> List[Dict[str, Any]]:
        """
        判断指定字符串是否在某个json数据的key中，可以是key的一部分,如果在则返回键和对应的值
        :param data: json数据
        :param special_str: 指定的字符串
        :return:
        """
        keysAll_list: List[Dict] = []

        def get_key(data, special_str):  # 遍历json所有key
            if type(data) == type({}):
                keys = data.keys()
                for key in keys:  # 不处理键为非字符串的，直接跳过了  123:{'zxc':wq}
                    value = data.get(key)
                    if isinstance(key, str):  # 如果键为字符串，则是键的一部分
                        if special_str in key:
                            keysAll_list.append({key: value})

                    if type(value) != type({}) and type(value) != type([]):  # 非列表非字典，则退出
                        continue
                    elif type(value) == type({}):
                        get_key(value, special_str)
                    elif type(value) == type([]):
                        for para in value:
                            if isinstance(para, dict):
                                get_key(para, special_str)

        get_key(data, special_str)
        return keysAll_list

    @classmethod
    def judge_special_str_in_json_value(cls, data: Dict, special_str: str) -> bool:
        """
        判断指定字符串是否在某个json数据的value中，可以是value的一部分
        :param data: json数据
        :param special_str: 指定的字符串
        :return:
        """
        if not data: return False
        value = jsonpath.jsonpath(data, '$..*')  # 将value按层展开
        for per in value:
            if not isinstance(per, type(special_str)): continue
            if special_str in per:
                return True
        else:
            return False

    @classmethod
    def judge_special_str_in_json(cls, data, special_str: str) -> bool:
        """
        判断指定字符串是否在某个json数据，是对前两个函数的封装
        :param data: json数据
        :param special_str: 指定的字符串
        :return:
        """
        result_key = JsonProcessor.judge_special_str_in_json_key(data, special_str)
        result_value = JsonProcessor.judge_special_str_in_json_value(data, special_str)
        if result_key or result_value:
            return True
        else:
            return False


